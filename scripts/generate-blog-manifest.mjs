import { readdirSync, writeFileSync } from "node:fs";
import path from "node:path";

const root = process.cwd();
const articlesDir = path.join(root, "lib", "blog", "articles");
const outFile = path.join(root, "lib", "blog", "articles.manifest.ts");

const files = readdirSync(articlesDir)
  .filter((name) => name.endsWith(".tsx"))
  .sort((a, b) => a.localeCompare(b));

const importLines = files.map((file, i) => {
  const noExt = file.replace(/\.tsx$/, "");
  return `import * as m${i} from "./articles/${noExt}";`;
});

const entries = files.map((file, i) => `  { sourceFile: "${file}", module: m${i} },`);

const content = `/**
 * AUTO-GENERATED FILE. DO NOT EDIT DIRECTLY.
 * Generated by scripts/generate-blog-manifest.mjs
 */
${importLines.join("\n")}

export const BLOG_ARTICLE_MODULE_ENTRIES = [
${entries.join("\n")}
] as const;
`;

writeFileSync(outFile, content, "utf8");
console.log(`[generate-blog-manifest] Wrote ${path.relative(root, outFile)} with ${files.length} article modules.`);
